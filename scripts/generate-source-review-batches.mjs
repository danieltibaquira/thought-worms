import fs from 'node:fs';
import path from 'node:path';
import {
  readJson,
  writeJson,
  normalizeText,
  assertValidArtifact
} from './pipeline-lib.mjs';

const crawlReportPath = process.argv[2] || 'data/source-crawl-report.json';
const batchDir = process.argv[3] || 'data/candidate-batches';
const generationReportPath = process.argv[4] || 'data/source-candidate-generation-report.json';

const report = readJson(crawlReportPath, { results: [] });
const sources = readJson('data/sources.json', []);
const sourcesById = new Map(sources.map((source) => [source.id, source]));
const now = new Date().toISOString();

function slug(value) {
  return String(value || '')
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 80) || 'source';
}

function trimWords(value, maxWords = 24) {
  const words = String(value || '')
    .replace(/\s+/g, ' ')
    .trim()
    .split(' ')
    .filter(Boolean);
  return words.slice(0, maxWords).join(' ');
}

function inferTheme(result, sample) {
  const text = `${sample || ''} ${(result.preferredThemeHits || []).join(' ')}`.toLowerCase();
  for (const theme of result.preferredThemeHits || []) {
    if (text.includes(String(theme).toLowerCase())) return theme;
  }
  return result.preferredThemeHits?.[0] || 'source review';
}

function sourceTitle(result) {
  return result.title || result.pageTitle || result.sourceId;
}

function candidateFromSample(result, sample, index) {
  const source = sourcesById.get(result.sourceId);
  const maxOriginalWords = Number(result.safety?.maxOriginalWords || 24);
  const theme = inferTheme(result, sample);
  const original = trimWords(sample, maxOriginalWords);
  const label = `${sourceTitle(result)} — ${theme} fragment ${index + 1}`;
  const row = {
    id: `${slug(result.sourceId)}-${slug(theme)}-${String(index + 1).padStart(3, '0')}`,
    sourceId: result.sourceId,
    status: 'review-needed',
    category: result.category || source?.category || 'Philosophical Fragment',
    artifact: label,
    original,
    english: `Review this public-source fragment as a possible ${theme} artifact.`,
    spanish: `Revisar este fragmento de fuente pública como posible artefacto de ${theme}.`,
    recommendedEditionSource: sourceTitle(result),
    sourceUrl: result.url || source?.url,
    notes: `Generated by source-review batch generator from ${crawlReportPath}. Status is review-needed, not verified. Human curation must replace this placeholder rendering before promotion.`
  };
  assertValidArtifact(row, index, sourcesById, { requireVerified: false });
  return row;
}

function writeBatch(result, rows) {
  if (!rows.length) return null;
  const safeName = `${slug(result.sourceId)}-review-batch-001.json`;
  const filePath = path.join(batchDir, safeName);
  writeJson(filePath, rows);
  return filePath;
}

fs.mkdirSync(batchDir, { recursive: true });

const outputs = [];
const seenIds = new Set();
for (const result of report.results || []) {
  const source = sourcesById.get(result.sourceId);
  const samples = Array.isArray(result.sampleWindows) ? result.sampleWindows : [];
  const limit = Math.max(0, Number(result.candidateLimit || 0));
  const canGenerate = source && limit > 0 && samples.length > 0 && !String(result.status || '').includes('deferred');

  if (!canGenerate) {
    outputs.push({
      sourceId: result.sourceId,
      generated: 0,
      reason: !source ? 'source-not-registered' : limit <= 0 ? 'candidate-limit-zero' : samples.length === 0 ? 'no-sample-windows' : 'not-generatable-status',
      status: result.status || null
    });
    continue;
  }

  const rows = [];
  for (const sample of samples.slice(0, limit)) {
    const row = candidateFromSample(result, sample, rows.length);
    if (seenIds.has(row.id)) continue;
    if (!normalizeText(row.original)) continue;
    seenIds.add(row.id);
    rows.push(row);
  }

  const filePath = writeBatch(result, rows);
  outputs.push({
    sourceId: result.sourceId,
    generated: rows.length,
    filePath,
    status: result.status || null
  });
}

writeJson(generationReportPath, {
  generatedAt: now,
  crawlReportPath,
  batchDir,
  totalGenerated: outputs.reduce((sum, output) => sum + output.generated, 0),
  outputs
});

console.log(`Generated ${outputs.reduce((sum, output) => sum + output.generated, 0)} review-needed candidate rows.`);
